# Vamos pensar em uma situação em que você só irá para o colégio se estiver sol e se for segunda feira

print(True and True)
print(True and False)
print(False and False)

# Vamos pensar em uma situação em que você só irá para o colégio se estiver sol ou se for segunda feira

print(True or True)
print(True or False)
print(False or False)
print(False or False)

#Operador de negação

print(not True)
print(not False)

#Operações Bit-a-bit

print(True & True)
print(False | True)